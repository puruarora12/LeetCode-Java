class Solution {
    public boolean isPalindrome(int x) {
        if(x<0)
         return false;
        else{
         int reverse = 0;
         int number = x ;   
            while(number >0){
                reverse = reverse*10+number%10;
                number/=10;
            }
            return (reverse==x ? true : false) ;
                
        }
    }
}
